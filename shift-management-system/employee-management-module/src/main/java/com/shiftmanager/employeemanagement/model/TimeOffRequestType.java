package com.shiftmanager.employeemanagement.model;

/**
 * Enum representing the possible types of time off requests
 */
public enum TimeOffRequestType {
    VACATION,        // Paid vacation time
    SICK_LEAVE,      // Sick leave
    PERSONAL_LEAVE,  // Personal leave
    BEREAVEMENT,     // Bereavement leave
    UNPAID_LEAVE,    // Unpaid leave of absence
    JURY_DUTY,       // Jury duty
    OTHER            // Other types of leave
}
